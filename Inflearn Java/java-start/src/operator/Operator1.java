package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        // 1. 덧셈
        int sum = a + b;
        System.out.println("a + b = " + sum);

        // 4. 나눗셈
        int div = 5 / 2;
        System.out.println("a / b = " + div);

        // 5. 나머지 연산
        int mod = 5 % 2;
        System.out.println("a % b = " + mod);
    }
}
